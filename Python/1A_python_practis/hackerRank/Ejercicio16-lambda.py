'''
Características de las Funciones Lambda
1. Anónimas: No tienen nombre (a menos que se asignen a una variable).
2. Cortas: Están diseñadas para ser usadas en una sola línea.
3. Limitadas: Solo pueden contener una expresión, no pueden incluir declaraciones como if, for, o while.
4. Flexibles: Se pueden usar en combinación con funciones como map(), filter(), y sorted().
'''

# la sume de dos numeros
suma = lambda x, y: x + y

print(suma(2, 4))

