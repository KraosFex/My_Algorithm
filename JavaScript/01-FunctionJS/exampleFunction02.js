// En el ejemplo anterior se vi como crear e invocar una funcion
// Pero esta no es la unica manera, ni mucho menos la mas facil.

// A esto se le denomina declaracion de funcion.
// es mucho mas facil de escribir y no requiere de punto y coma al final
// como la funcion del ejemplo anterior.
function cuadrado(x) {
    return x * x
}

// A esto se le llama funcion flecha.
// Esta es otra manera de crear una funcion, seria la siguiente.
const cuadrado = x => x * x

// Esto claramente es una manera mas resumina de crear la misma funcion de arriba.
// aunque quisa no se la manera mas facil e intuitiva de crear una funcion.
// y es una recomendacion personal usarla, pero cuando se tenga un nivel de abstraccion 
// mas desarrollado en el lenguaje.
